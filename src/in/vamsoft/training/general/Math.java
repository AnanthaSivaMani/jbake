package in.vamsoft.training.general;

public class Math {

    /**
     * @param a.
     * @param b.
     * @return This method calculate the sum of two numbers and returns int.
     */
    public int sum(int a, int b) {
        return a + b;
    }

    /**
     * @param a.
     * @param b.
     * @return This method calculate the sum of two numbers and returns double.
     */
    public double sum(double a, double b) {
        return a + b;
    }

}
